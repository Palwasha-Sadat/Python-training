sales = [30000, 32000, 25000, 40000]
quote = "Turn could've, would've, and should've into can, will, and do."

# Check if 25000 is in the sales list
print(25000 in sales)  # True because 25000 is one of the elements in the sales list

# Check if "do" is in the quote
print("do" in quote)  # True because "do" is a substring in the quote

# Check if "did" is in the quote
print("did" in quote)  # False because "did" is not a substring in the quote

