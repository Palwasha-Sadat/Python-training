# Assign values to variables
a = 15
b = 25
c = 15

# Check if a is equal to b
print(a == b)  # False

# Check if a is equal to c
print(a == c)  # True

# Check if a is greater than c
print(a > c)  # False

# Check if a is greater than or equal to c
print(a >= c)  # True

# Check if a is less than or equal to c
print(a <= c)  # True

# Check if a is not equal to c
print(a != c)  # False

# Perform a mathematical operation and assign the result to z
z = a * (b + c) / a

# Print the value of z
print(z)
