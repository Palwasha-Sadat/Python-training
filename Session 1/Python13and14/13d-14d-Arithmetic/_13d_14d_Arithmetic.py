a = 8
b = 3

# Arithmetic print statements
print("a + b =", a + b)  # Addition: 8 + 3 = 11
print("a - b =", a - b)  # Subtraction: 8 - 3 = 5
print("a * b =", a * b)  # Multiplication: 8 * 3 = 24
print("a / b =", a / b)  # Division: 8 / 3 = 2.6666666666666665
print("a // b =", a // b)  # Integer Division: 8 // 3 = 2
print("a % b =", a % b)  # Modulus (Remainder): 8 % 3 = 2
print("a ** b =", a ** b)  # Exponentiation: 8 ** 3 = 512
